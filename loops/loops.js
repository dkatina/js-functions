

// Basic For Loop (similar to a python range loop)
//      start     stop  step
for (let i = 0; i < 10; i += 5){
    console.log(i)
}

fruits = ["apple", "pear", "tomato"]

for (let i = 0; i < fruits.length; i++){
    console.log(i, fruits[i])
}


// While loop

n = 10
while (n > 0){
    console.log(n)
    n--
};







